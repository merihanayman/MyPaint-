    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        int i = jComboBox1.getSelectedIndex();

        try {
            MyCanvas c = (MyCanvas) jPanel2;
            Shape s = (Shape) c.shapesList.get(i).getClass().newInstance();
            Color c1 = (Color) c.shapesList.get(i).getColor();
            s.setPosition(c.shapesList.get(i).getPosition());

            c.shapesList.remove(i);
            c.shapesList.add(i, s);
            c.shapesList.get(i).setColor(c1);
            c.repaint();
        } catch (InstantiationException | IllegalAccessException ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton14ActionPerformed

