    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int result;
        int i = jComboBox1.getSelectedIndex();
        try {
            MyCanvas c = (MyCanvas) jPanel2;
            JPanel panel = new JPanel();
            JColorChooser colorChooser = new JColorChooser(c.shapesList.get(i).getColor());
            panel.add(colorChooser);
            result = JOptionPane.showConfirmDialog(null, panel, "Choose Color", JOptionPane.OK_CANCEL_OPTION);
            if (jRadioButton1.isSelected() == true) {
                for (Shape s : shapesList) {
                    s.setColor(colorChooser.getColor());
                }
            } else {
                c.shapesList.get(i).setColor(colorChooser.getColor());
            }

            c.repaint();

        } catch (Exception E) {
            System.out.println(E);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

